				{
					model = new university.Model.CCOM.User_notice();					
													if(dt.Rows[n]["id"].ToString()!="")
				{
					model.id=long.Parse(dt.Rows[n]["id"].ToString());
				}
																																if(dt.Rows[n]["User_id"].ToString()!="")
				{
					model.User_id=long.Parse(dt.Rows[n]["User_id"].ToString());
				}
																																				model.Notice_id= dt.Rows[n]["Notice_id"].ToString();
																																model.Notice_read_id= dt.Rows[n]["Notice_read_id"].ToString();
																						
				
					modelList.Add(model);
				}
			}
			return modelList;
		}

